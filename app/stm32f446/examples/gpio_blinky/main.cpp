#include <Utils.h>

#include <GPIO.h>

////////////////////////////////////////////////////////////////////////////////
// Main!
////////////////////////////////////////////////////////////////////////////////

extern "C" void main() {
  GPIO_A.enable();
  GPIO_A.setMode(1, GPIO::PinMode::OUTPUT);

  while (true) {
    GPIO_A.set(1);
    DELAY(1000000);
    GPIO_A.clear(1);
    DELAY(1000000);
  }
}
